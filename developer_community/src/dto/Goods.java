package dto;

public class Goods {

	private String goods_code;
	private String id;
	private String goods_title;
	private String goods_price;
	private String goods_info;
	private String goods_date;
	private String goods_img;
	
	public String getGoods_code() {
		return goods_code;
	}
	public void setGoods_code(String goods_code) {
		this.goods_code = goods_code;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGoods_title() {
		return goods_title;
	}
	public void setGoods_title(String goods_title) {
		this.goods_title = goods_title;
	}
	public String getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(String goods_price) {
		this.goods_price = goods_price;
	}
	public String getGoods_info() {
		return goods_info;
	}
	public void setGoods_info(String goods_info) {
		this.goods_info = goods_info;
	}
	public String getGoods_date() {
		return goods_date;
	}
	public void setGoods_date(String goods_date) {
		this.goods_date = goods_date;
	}
	public String getGoods_img() {
		return goods_img;
	}
	public void setGoods_img(String goods_img) {
		this.goods_img = goods_img;
	}
	public String getGoods_inquiry() {
		return goods_inquiry;
	}
	public void setGoods_inquiry(String goods_inquiry) {
		this.goods_inquiry = goods_inquiry;
	}
	private String goods_inquiry;
	

}
